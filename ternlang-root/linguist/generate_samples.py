def ensemble_vote(n: int, seed: int) -> str:
    """N-agent ensemble vote with majority gate"""
    rng = random.Random(seed)
    domains = rng.sample(
        ["expert_a", "expert_b", "expert_c", "expert_d", "expert_e",
         "expert_f", "expert_g", "expert_h", "expert_i", "expert_j"], k=min(n, 10)
    )
    body = "\n".join(f"    let {d}: trit = signal;" for d in domains)
    fold = domains[0]
    for d in domains[1:]:
        fold = f"consensus({fold}, {d})"
    return f"""{HEADER}
// Category: ensemble_vote | n={n} | seed={seed}
// N-expert ensemble voting — ternary consensus across {n} domain signals.

fn ensemble_{n}_{seed}(signal: trit) -> trit {{
{body}
    let result: trit = {fold};
    return result;
}}
"""


