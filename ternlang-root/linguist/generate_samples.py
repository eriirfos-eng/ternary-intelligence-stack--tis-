def generate(count: int, out_dir: str, dry_run: bool = False) -> int:
    out = Path(out_dir)
    written = 0
    rng = random.Random(42)

    # Cycle through categories evenly, vary seed per file
    for i in range(count):
        cat_idx = i % len(CATEGORIES)
        subdir, prefix, gen_fn = CATEGORIES[cat_idx]
        seed = rng.randint(0, 2**31)

        try:
            source = gen_fn(seed)
        except Exception as e:
            print(f"[WARN] generator {prefix} seed={seed} failed: {e}", file=sys.stderr)
            continue

        dest_dir = out / subdir
        if not dry_run:
            dest_dir.mkdir(parents=True, exist_ok=True)

        filename = f"{prefix}_{seed:010d}.tern"
        dest = dest_dir / filename

        if not dry_run:
            dest.write_text(source, encoding="utf-8")

        written += 1
        if written % 100 == 0:
            print(f"  {written}/{count} files written...", flush=True)

    return written


