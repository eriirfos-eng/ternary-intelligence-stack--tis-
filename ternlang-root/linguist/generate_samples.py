def trit_arith(n: int, seed: int) -> str:
    """Balanced ternary arithmetic: addition circuits"""
    rng = random.Random(seed)
    ops = rng.choices(["consensus", "invert", "truth", "conflict", "hold"], k=n)
    body = "\n".join(
        f"    let v{i}: trit = {op}(v{i-1});" if i > 0 else f"    let v{i}: trit = {op}();"
        for i, op in enumerate(ops)
    )
    return f"""{HEADER}
// Category: balanced_ternary_arithmetic | seed={seed}
// Demonstrates chained trit operations on the BET VM.

fn trit_chain_{seed}(seed: trit) -> trit {{
    let v0: trit = seed;
{body}
    return v{n-1};
}}
"""


def sparse_gate(dim: int, seed: int) -> str:
    """@sparseskip sparse matmul gate"""
    return f"""{HEADER}
// Category: sparse_matmul_gate | dim={dim}x{dim} | seed={seed}
// @sparseskip skips zero-trit (tend) cells — the flagship BET AI primitive.
// Achieves up to 2.3x fewer multiply operations at 56% sparsity.

@sparseskip
fn sparse_gate_{dim}_{seed}(a: trittensor<{dim} x {dim}>, b: trittensor<{dim} x {dim}>) -> trittensor<{dim} x {dim}> {{
    let result: trittensor<{dim} x {dim}> = matmul(a, b);
    return result;
}}
"""


def decision_gate(name: str, seed: int) -> str:
    """Three-way safety-interlock decision gate"""
    rng = random.Random(seed)
    desc = rng.choice(["safety", "compliance", "authorization", "readiness", "quorum"])
    fallback = rng.choice(["conflict()", "hold()"])
    return f"""{HEADER}
// Category: decision_gate | domain={desc} | seed={seed}
// Three-way exhaustive match — the compiler enforces all arms are present.

fn {name}_{seed}({desc}: trit, signal: trit) -> trit {{
    match {desc} {{
        -1 => {{ return conflict(); }}
         0 => {{
            match signal {{
                -1 => {{ return conflict(); }}
                 0 => {{ return hold(); }}
                 1 => {{ return {fallback}; }}
            }}
         }}
         1 => {{ return signal; }}
    }}
}}
"""


def neurosym_template(layers: int, seed: int) -> str:
    """Neurosymbolic ternary inference chain"""
    body = "\n".join(
        f"    let layer{i}: trit = consensus(layer{i-1}, signal);" if i > 0
        else "    let layer0: trit = signal;"
        for i in range(layers)
    )
    return f"""{HEADER}
// Category: neurosymbolic_inference | layers={layers} | seed={seed}
// Ternary neural inference: each layer applies ternary consensus.
// Tend (0) propagates hold — information must be definitive to pass.

fn infer_{layers}_{seed}(signal: trit) -> trit {{
{body}
    match layer{layers-1} {{
        -1 => {{ return reject; }}
         0 => {{ return tend; }}
         1 => {{ return affirm; }}
    }}
}}
"""


def signal_proc(channels: int, seed: int) -> str:
    """Ternary signal processing with hold-propagation"""
    rng = random.Random(seed)
    domain = rng.choice(["sensor", "audio", "radar", "eeg", "lidar", "sonar"])
    return f"""{HEADER}
// Category: signal_processing | domain={domain} | channels={channels} | seed={seed}
// Multi-channel ternary signal fusion.
// Hold (tend) means "signal insufficient for decision" — not absence of data.

fn fuse_{domain}_{channels}_{seed}(
    ch0: trit, ch1: trit{"".join(f", ch{i}: trit" for i in range(2, channels))}
) -> trit {{
    let fused: trit = consensus(ch0, ch1);
{"".join(f"    let fused: trit = consensus(fused, ch{i});{chr(10)}" for i in range(2, channels))}
    match fused {{
        -1 => {{ return conflict(); }}
         0 => {{ return hold(); }}
         1 => {{ return truth(); }}
    }}
}}
"""


def actor_model(role: str, seed: int) -> str:
    """Actor model: agent spawn + send + await"""
    return f"""{HEADER}
// Category: actor_model | role={role} | seed={seed}
// Ternary actor: spawns a {role} agent, sends a signal, awaits ternary reply.

agent {role.capitalize()}Agent_{seed} {{
    fn handle(signal: trit) -> trit {{
        match signal {{
            -1 => {{ return conflict(); }}
             0 => {{ return hold(); }}
             1 => {{ return truth(); }}
        }}
    }}
}}

fn run_{role}_{seed}() -> trit {{
    let agent: agentref = spawn {role.capitalize()}Agent_{seed};
    send agent affirm;
    let result: trit = await agent;
    return result;
}}
"""


def hdl_gate(width: int, seed: int) -> str:
    """HDL-targeting ternary register operations"""
    rng = random.Random(seed)
    ops = rng.choices(["invert", "truth", "hold", "conflict"], k=width)
    body = "\n".join(f"    let reg{i}: trit = {op}();" for i, op in enumerate(ops))
    return f"""{HEADER}
// Category: hdl_register | width={width} | seed={seed}
// Ternary register bank simulation — targets ternlang-hdl Verilog codegen.
// Each register is a 2-bit packed trit: 0b01=-1  0b10=+1  0b11=0

fn reg_bank_{width}_{seed}() -> trit {{
{body}
    let result: trit = consensus(reg0, reg1);
    return result;
}}
"""


def ml_quantize(bits: int, seed: int) -> str:
    """BitNet-style ternary quantization"""
    return f"""{HEADER}
// Category: ml_quantization | precision={bits}bit | seed={seed}
// Ternary weight quantization — maps float weights to {{-1, 0, +1}}.
// The @sparseskip directive skips zero-weight neurons in forward pass.

@sparseskip
fn quantize_layer_{bits}_{seed}(weight: trit, activation: trit) -> trit {{
    let q: trit = consensus(weight, activation);
    match q {{
        -1 => {{ return reject; }}
         0 => {{ return tend; }}
         1 => {{ return affirm; }}
    }}
}}
"""


def stdlib_math(op: str, seed: int) -> str:
    """Standard library math operations"""
    ops = {
        "abs":       "match x { -1 => { return truth(); } 0 => { return hold(); } 1 => { return truth(); } }",
        "neg":       "match x { -1 => { return truth(); } 0 => { return hold(); } 1 => { return conflict(); } }",
        "majority":  "let m: trit = consensus(a, b); return consensus(m, c);",
        "threshold": "match x { -1 => { return conflict(); } 0 => { return hold(); } 1 => { return truth(); } }",
        "clamp":     "match x { -1 => { return conflict(); } 0 => { return hold(); } 1 => { return truth(); } }",
    }
    chosen_op = list(ops.keys())[seed % len(ops)]
    impl = ops[chosen_op]
    params = "x: trit" if chosen_op not in ("majority",) else "a: trit, b: trit, c: trit"
    return f"""{HEADER}
// Category: stdlib_math | op={chosen_op} | seed={seed}

fn {chosen_op}_{seed}({params}) -> trit {{
    {impl}
}}
"""


def reasoning_chain(depth: int, seed: int) -> str:
    """Deliberation/reasoning chain (ternlang-moe style)"""
    rng = random.Random(seed)
    agents = rng.sample([
        "validator", "planner", "critic", "synthesizer", "arbiter",
        "observer", "guardian", "resolver", "analyst", "auditor"
    ], k=min(depth, 10))
    body = "\n".join(
        f"    let {a}: trit = consensus(signal, hold());" for a in agents
    )
    fold = " ".join(f"consensus({agents[i]}, {agents[i+1]})" for i in range(len(agents)-1))
    if not fold:
        fold = agents[0]
    return f"""{HEADER}
// Category: reasoning_chain | depth={depth} | seed={seed}
// Multi-agent deliberation: each sub-agent votes, result is ternary consensus.
// Modeled on ternlang-moe MoE-13 architecture (DOI 10.17605/OSF.IO/TZ7DC).

fn deliberate_{depth}_{seed}(signal: trit) -> trit {{
{body}
    let result: trit = {fold};
    match result {{
        -1 => {{ return reject; }}
         0 => {{ return tend; }}
         1 => {{ return affirm; }}
    }}
}}
"""


def safety_gate(domain: str, seed: int) -> str:
    """Hard-gate safety interlock"""
    return f"""{HEADER}
// Category: safety_interlock | domain={domain} | seed={seed}
// Safety is evaluated FIRST — a reject veto is unconditional.
// This mirrors the action_gate() primitive in ternlang-ml reasoning toolkit.

fn safety_{domain}_{seed}(
    safety: trit,
    authorization: trit,
    capability: trit
) -> trit {{
    match safety {{
        -1 => {{ return reject; }}   // Hard veto — no override
         0 => {{
            match authorization {{
                -1 => {{ return reject; }}
                 0 => {{ return tend; }}
                 1 => {{ return tend; }}  // Safety uncertain → hold
            }}
         }}
         1 => {{
            return consensus(authorization, capability);
         }}
    }}
}}
"""


def struct_model(name: str, fields: int, seed: int) -> str:
    """Struct definition with field access"""
    field_defs = "\n".join(f"    f{i}: trit," for i in range(fields))
    field_reads = "\n".join(f"    let r{i}: trit = s.f{i};" for i in range(fields))
    fold = "consensus(r0, r1)" if fields >= 2 else "r0"
    return f"""{HEADER}
// Category: struct_model | name={name} | fields={fields} | seed={seed}

struct {name}_{seed} {{
{field_defs}
}}

fn read_{name.lower()}_{seed}(s: {name}_{seed}) -> trit {{
{field_reads}
    return {fold};
}}
"""


